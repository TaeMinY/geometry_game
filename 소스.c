int GetKey(int map[MAPSIZEY][MAPSIZEX], int type)
{
		int key;
		if (map[y][x + 1] != WALL)//오른쪽 move
		{
			map[y][x] = 0;
			if (map[y][x + 1] == END)
			{
				//change
				playerscore = (30 * type) + playerscore;
				map[9][2] = PLAYER;
				return 1;
			}
			map[y][x + 1] = 2;
			x += 1;
		}
		if (map[y + 1][x] != 1) //down
		{
			map[y][x] = 0;
			if (map[y + 1][x] == END)
			{
				playerscore = (30 * type) + playerscore;
				map[9][2] = PLAYER;
				return 1;
			}
			map[y + 1][x] = 2;
			y += 1;
		}
		

		if (_kbhit())
		{
			key = _getch();

			if (key == 32 && map[y - 1][x] != 1 && map[y - 2][x] != 1)//jump
			{
				map[y][x] = 0;
				map[y - 2][x] = 2;
				y -= 2;
			}
			

			//esc
			if (key == 27) {
				for (int i = 0; i < MAPSIZEY; i++) {
					for (int j = 0; j < MAPSIZEX; j++) {
						if (map[i][j] == PLAYER) {
							map[i][j] = 0;
							break;
						}
					}
				}
				map[9][2] = PLAYER;
				return 1;
			}
		}
		
		return 0;
	
}

